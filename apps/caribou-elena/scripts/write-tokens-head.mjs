#!/usr/bin/env node
import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

// Bake @beatzball/caribou-design-tokens/tokens.css into a module-level string
// constant so the server bundle has no runtime filesystem lookup. The
// deployed Docker image only ships `dist/` (no node_modules), so any
// `require.resolve`/`readFileSync` of a workspace package at module init
// throws MODULE_NOT_FOUND and 500s every page request.
const appRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const repoRoot = resolve(appRoot, '..', '..')

const tokensCss = readFileSync(
  resolve(repoRoot, 'packages/design-tokens/tokens.css'),
  'utf8',
)

const target = resolve(appRoot, 'server/lib/tokens-head.generated.ts')
const next = `// AUTO-GENERATED by scripts/write-tokens-head.mjs — do not edit.
export const TOKENS_CSS = ${JSON.stringify(tokensCss)}
`

const prev = existsSync(target) ? readFileSync(target, 'utf8') : null
if (prev !== next) {
  writeFileSync(target, next)
  console.log(`[tokens-head] inlined ${tokensCss.length} bytes`)
}
