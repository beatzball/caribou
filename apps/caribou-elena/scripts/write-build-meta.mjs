#!/usr/bin/env node
import { execSync } from 'node:child_process'
import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const appRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')

function safe(fn, fallback) {
  try { return fn() } catch { return fallback }
}

const commit =
  process.env.GIT_SHA?.trim() ||
  safe(
    () => execSync('git rev-parse HEAD', { cwd: appRoot, stdio: ['ignore', 'pipe', 'ignore'] }).toString().trim(),
    'unknown',
  )
const pkg = JSON.parse(readFileSync(resolve(appRoot, 'package.json'), 'utf8'))
const version = pkg.version ?? 'unknown'

const target = resolve(appRoot, 'server/build-meta.generated.ts')
const next = `// AUTO-GENERATED by scripts/write-build-meta.mjs — do not edit.
export const GIT_SHA = ${JSON.stringify(commit)}
export const PACKAGE_VERSION = ${JSON.stringify(version)}
`

// Skip writing when content is unchanged so repeated dev/test runs don't
// thrash the mtime or show up as uncommitted diffs.
const prev = existsSync(target) ? readFileSync(target, 'utf8') : null
if (prev !== next) {
  writeFileSync(target, next)
  console.log(`[build-meta] commit=${commit.slice(0, 8)} version=${version}`)
}
