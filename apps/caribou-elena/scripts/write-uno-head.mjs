#!/usr/bin/env node
// Generate the inlined UnoCSS <style> block injected into every SSR'd
// page's <head>. Same pattern as write-tokens-head.mjs: scan source files
// for utility classes, generate CSS via the configured presets, write a
// TS string constant. The deployed Docker image only ships `dist/`, so any
// runtime UnoCSS import would 500 every page request — bake it in at build.
//
// Node 24+ strips TypeScript natively; we import the .ts uno.config directly.
import { existsSync, readFileSync, readdirSync, statSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { createGenerator } from 'unocss'
import unoConfig from '../uno.config.ts'

const appRoot  = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const repoRoot = resolve(appRoot, '..', '..')

function walk(dir, exts, acc = []) {
  if (!existsSync(dir)) return acc
  for (const name of readdirSync(dir)) {
    const full = resolve(dir, name)
    const s = statSync(full)
    if (s.isDirectory()) {
      if (name === 'node_modules' || name.startsWith('.')) continue
      walk(full, exts, acc)
    } else if (exts.some((e) => name.endsWith(e))) {
      acc.push(full)
    }
  }
  return acc
}

// Collect content sources matching uno.config.ts `content.filesystem`.
// Hand-mirrored here to avoid pulling a glob library; both lists must
// stay in sync with the config above.
const sources = [
  ...walk(resolve(appRoot, 'pages'), ['.ts', '.html']),
  ...(existsSync(resolve(appRoot, 'app.ts')) ? [resolve(appRoot, 'app.ts')] : []),
  ...walk(resolve(repoRoot, 'packages'), ['.ts']).filter((p) => p.includes('/src/')),
]

const text = sources.map((p) => readFileSync(p, 'utf8')).join('\n')

const uno = await createGenerator(unoConfig.default ?? unoConfig)
const { css } = await uno.generate(text, { preflights: true, minify: true })

const target = resolve(appRoot, 'server/lib/uno-head.generated.ts')
const next = `// AUTO-GENERATED by scripts/write-uno-head.mjs — do not edit.
export const UNO_CSS = ${JSON.stringify(css)}
`

const prev = existsSync(target) ? readFileSync(target, 'utf8') : null
if (prev !== next) {
  writeFileSync(target, next)
  console.log(`[uno-head] generated ${css.length} bytes from ${sources.length} sources`)
}
